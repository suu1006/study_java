package cp7;

public class cp7_7_Product {

	public static void main(String[] args) {
		Buyer b = new Buyer();

		b.buy(new Tv1()); // Tv t = new Tv1(); + b.buy(t) 문장
		b.buy(new Computer()); // buy(Product p) 호출한 것

		System.out.println("현재 남은 돈은" + b.money + "만원입니다.");
		System.out.println("현재 보너스점수는 " + b.bonusPoint + "점입니다.");
	}

}
class Product {
	int price;
	int bonusPoint;

	Product(int price) {
		this.price = price;
		bonusPoint = (int) (price / 10.0);
	}
}

class Tv1 extends Product {
	Tv1() {
		super(100); // tv의 가격은 100만원 // 조상클래스의 생성자 Product(int price)를 호출
	}

	public String toString() {
		return "Tv";
	}
}

class Computer extends Product {
	Computer() {
		super(200);
	}

	public String toString() {
		return "Computer";
	}
}

class Buyer {
	int money = 1000;
	int bonusPoint = 0;

	void buy(Product p) {
		if (money < p.price) {
			System.out.println("잔액이 부족하여 물건을 살 수 없습니다.");
			return;
		}
		money -= p.price; // 가진 돈에서 구입한 제품의 가격을 뺌
		bonusPoint += p.bonusPoint; // 제품의 보너스 점수를 추가한다.
		System.out.println(p + "을/를 구입하셨습니다.");
//		System.out.println(p.toString() + "을/를 구입하셨습니다."); 위 문장과 같은 문장임.
	}
}