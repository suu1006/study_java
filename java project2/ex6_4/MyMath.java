package ex6_4;

public class MyMath {
	

		long add(long a, long b) {
		long result = a + b;
		return result;}
			
		long subtract(long a, long b) {
		long result = a - b;
		return result;}
		
				
		long multiply(long a, long b) {
		long result = a * b;
		return result;}				
		
		
		long divide(long a, long b) {
		long result = a / b;
		return result;}
		
}