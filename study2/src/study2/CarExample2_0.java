package study2;

public class CarExample2_0 {
	public static void main(String[] args) {
		Car1_8 car = new Car1_8();

		for (int i = 1; i <= 5; i++) {
			int problemLocation = car.run();

			switch (problemLocation) {
			case 1:
				System.out.println("앞왼쪽 HankookTire로 교체 ");
				car.frontLeftTire = new HankookTire("앞왼쪽", 15);
				break;

			case 2:
				System.out.println("앞오른쪽 HankookTire로 교체 ");
				car.frontRightTire = new HankookTire("앞오른쪽", 15);
				break;
			case 3:
				System.out.println("뒤왼쪽 HankookTire로 교체 ");
				car.backLeftTire = new HankookTire("뒤왼쪽", 15);
				break;
			case 4:
				System.out.println("뒤오른쪽 HankookTire로 교체 ");
				car.backRightTire = new HankookTire("뒤오른쪽", 15);
				break;
			}
			System.out.println("=============================");
		}
	}
}
